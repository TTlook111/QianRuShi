/*********************************************************************************************
* 条件编译
*********************************************************************************************/
#ifndef __HALL_H__
#define __HALL_H__

/*********************************************************************************************
* 头文件
*********************************************************************************************/
#include <ioCC2530.h>
#include "gpio_int.h"

/*********************************************************************************************
* 宏定义
*********************************************************************************************/

/* 霍尔传感器 GPIO 引脚定义 */
#define HALL_GPIO_PIN           GPIO_INT_PIN_P0_2   /* P0.2 */
#define HALL_GPIO_BIT           P0_2                 /* 直接位操作 */

/* 门磁状态定义 */
#define DOOR_STATE_CLOSED       0                    /* 门闭合 (霍尔=低电平) */
#define DOOR_STATE_OPEN         1                    /* 门打开 (霍尔=高电平) */

/* 霍尔中断触发边沿 */
#define HALL_INT_EDGE           GPIO_INT_FALLING_EDGE /* 下降沿 (门关闭时触发) */

/* 去抖参数 */
#define HALL_DEBOUNCE_MS        100                   /* 硬件去抖时间 (ms) */

/*********************************************************************************************
* 类型定义
*********************************************************************************************/

/**
 * 门状态变化回调
 * @param state  - DOOR_STATE_CLOSED (门闭合) / DOOR_STATE_OPEN (门打开)
 * @param pdata  - 用户数据
 */
typedef void (*door_event_callback_t)(uint8 state, void *pdata);

/*********************************************************************************************
* 内部原型函数
*********************************************************************************************/

/* ===== 轮询模式 API (保持兼容) ===== */

/**
 * @brief  初始化霍尔传感器 (轮询模式)
 * @note   配置 P0.2 为通用 IO 输入
 */
void hall_init(void);

/**
 * @brief  读取门磁状态 (轮询模式)
 * @return DOOR_STATE_OPEN (1) = 门打开, DOOR_STATE_CLOSED (0) = 门闭合
 */
unsigned char get_hall_status(void);


/* ===== 中断模式 API (新增) ===== */

/**
 * @brief  初始化霍尔传感器为 GPIO 中断模式
 * @param  callback - 门状态变化回调
 * @param  pdata    - 用户数据
 * @note   使能 P0.2 双边沿中断，实时感知门开关变化
 */
void hall_int_init(door_event_callback_t callback, void *pdata);

/**
 * @brief  使能门磁中断检测
 */
void hall_int_enable(void);

/**
 * @brief  禁止门磁中断检测
 */
void hall_int_disable(void);

/**
 * @brief  获取门磁中断触发次数 (测试用)
 */
uint16 hall_int_get_count(void);

/**
 * @brief  复位触发计数器
 */
void hall_int_reset_count(void);

#endif /* __HALL_H__ */
