/*********************************************************************************************
* 条件编译
*********************************************************************************************/
#ifndef __INFRARED_H__
#define __INFRARED_H__

/*********************************************************************************************
* 头文件
*********************************************************************************************/
#include <ioCC2530.h>
#include "gpio_int.h"

/*********************************************************************************************
* 宏定义
*********************************************************************************************/

/* PIR 传感器 GPIO 引脚定义 */
#define PIR_GPIO_PIN            GPIO_INT_PIN_P0_0   /* P0.0 */
#define PIR_GPIO_BIT            P0_0                 /* 直接位操作 */

/* PIR 触发配置 */
#define PIR_TRIGGER_LEVEL       1                    /* 高电平 = 有人 */
#define PIR_IDLE_LEVEL          0                    /* 低电平 = 无人 */
#define PIR_INT_EDGE            GPIO_INT_RISING_EDGE  /* 上升沿触发 (有人靠近时) */

/* PIR 抗抖动参数 */
#define PIR_DEBOUNCE_MS         200                  /* 硬件去抖时间 (ms) */

/*********************************************************************************************
* 类型定义
*********************************************************************************************/

/**
 * PIR 传感器事件回调 (应用层注册)
 * @param state  - 0=无人, 1=有人靠近
 * @param pdata  - 用户数据
 */
typedef void (*pir_event_callback_t)(uint8 state, void *pdata);

/*********************************************************************************************
* 内部原型函数
*********************************************************************************************/

/* ===== 轮询模式 API (保持兼容) ===== */

/**
 * @brief  初始化 PIR 传感器 (轮询模式)
 * @note   配置 P0.0 为通用 IO 输入
 */
void infrared_init(void);

/**
 * @brief  读取 PIR 传感器状态 (轮询模式)
 * @return 1 = 检测到人体, 0 = 无人
 */
unsigned char get_infrared_status(void);


/* ===== 中断模式 API (新增) ===== */

/**
 * @brief  初始化 PIR 传感器为 GPIO 中断模式
 * @param  callback - 中断触发时的回调函数
 * @param  pdata    - 用户数据
 * @note   配置 P0.0 下降沿中断触发 (PIR OUT 从高变低 = 人离开)
 *         若需检测"人靠近"事件, 配合轮询确认当前电平
 */
void infrared_int_init(pir_event_callback_t callback, void *pdata);

/**
 * @brief  使能 PIR 中断检测
 */
void infrared_int_enable(void);

/**
 * @brief  禁止 PIR 中断检测 (如配置模式/低功耗)
 */
void infrared_int_disable(void);

/**
 * @brief  获取 PIR 中断触发次数 (用于测试统计)
 * @return 累计触发次数
 */
uint16 infrared_int_get_count(void);

/**
 * @brief  复位中断触发计数器
 */
void infrared_int_reset_count(void);

#endif /* __INFRARED_H__ */
