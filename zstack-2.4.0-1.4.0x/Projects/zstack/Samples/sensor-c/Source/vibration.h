/*********************************************************************************************
* 条件编译
*********************************************************************************************/
#ifndef __VIBRATION_H__
#define __VIBRATION_H__

/*********************************************************************************************
* 头文件
*********************************************************************************************/
#include <ioCC2530.h>
#include "gpio_int.h"

/*********************************************************************************************
* 宏定义
*********************************************************************************************/

/* 震动传感器 GPIO 引脚定义 */
#define VIB_GPIO_PIN            GPIO_INT_PIN_P0_1   /* P0.1 */
#define VIB_GPIO_BIT            P0_1                 /* 直接位操作 */

/* 震动状态定义 */
#define VIB_STATE_IDLE          0                    /* 无震动 */
#define VIB_STATE_TRIGGERED     1                    /* 检测到震动 */

/* 中断触发边沿 */
#define VIB_INT_EDGE            GPIO_INT_FALLING_EDGE /* 下降沿 (震动=低电平脉冲) */

/* 去抖参数 */
#define VIB_DEBOUNCE_MS         200                   /* 硬件去抖时间 (ms) */

/*********************************************************************************************
* 类型定义
*********************************************************************************************/

/**
 * 震动事件回调
 * @param state  - VIB_STATE_TRIGGERED (1) = 检测到震动
 * @param pdata  - 用户数据
 */
typedef void (*vib_event_callback_t)(uint8 state, void *pdata);

/*********************************************************************************************
* 内部原型函数
*********************************************************************************************/

/* ===== 轮询模式 API (保持兼容) ===== */

/**
 * @brief  初始化震动传感器 (轮询模式)
 * @note   配置 P0.1 为通用 IO 输入
 */
void vibration_init(void);

/**
 * @brief  读取震动传感器状态 (轮询模式)
 * @return VIB_STATE_TRIGGERED (1) = 有震动, VIB_STATE_IDLE (0) = 无震动
 */
unsigned char get_vibration_status(void);


/* ===== 中断模式 API (新增) ===== */

/**
 * @brief  初始化震动传感器为 GPIO 中断模式
 * @param  callback - 震动事件回调
 * @param  pdata    - 用户数据
 */
void vibration_int_init(vib_event_callback_t callback, void *pdata);

/**
 * @brief  使能震动中断检测
 */
void vibration_int_enable(void);

/**
 * @brief  禁止震动中断检测
 */
void vibration_int_disable(void);

/**
 * @brief  获取震动中断触发次数
 */
uint16 vibration_int_get_count(void);

/**
 * @brief  复位触发计数器
 */
void vibration_int_reset_count(void);

#endif /* __VIBRATION_H__ */
