/*********************************************************************************************
* 名称：ZXBeeUserProcess()
* 功能：解析智云平台下发的查询命令，返回对应传感器数据
*       支持字段：temp(A0) / humi(A1) / lux(A2) / V0(上报周期)
*       支持 D0 位控制（CD0/OD0/D0）
* 参数：*ptag -- 数据标签
*       *pval -- 数据内容（"?" 表示查询）
* 返回：<0：未处理；>=0：已处理（返回值为响应数据长度）
*********************************************************************************************/
int ZXBeeUserProcess(char *ptag, char *pval)
{
  int val;
  int ret = 0;
  char pData[16];
  char *p = pData;

  val = atoi(pval);

  // ---- D0 位控制 ----
  if (0 == strcmp("CD0", ptag)) {
    D0 &= ~val;
    ret = sprintf(p, "%u", D0);
    ZXBeeAdd("D0", p);
  }
  if (0 == strcmp("OD0", ptag)) {
    D0 |= val;
    ret = sprintf(p, "%u", D0);
    ZXBeeAdd("D0", p);
  }
  if (0 == strcmp("D0", ptag)) {
    if (0 == strcmp("?", pval)) {
      ret = sprintf(p, "%u", D0);
      ZXBeeAdd("D0", p);
    }
  }

  // ---- 温度查询（支持 A0 和 temp 两种标签）----
  if (0 == strcmp("A0", ptag) || 0 == strcmp("temp", ptag)) {
    if (0 == strcmp("?", pval)) {
      updateA0();
      ret = sprintf(p, "%.1f", A0);
      ZXBeeAdd("temp", p);
    }
  }
  // ---- 湿度查询（支持 A1 和 humi 两种标签）----
  if (0 == strcmp("A1", ptag) || 0 == strcmp("humi", ptag)) {
    if (0 == strcmp("?", pval)) {
      updateA1();
      ret = sprintf(p, "%.1f", A1);
      ZXBeeAdd("humi", p);
    }
  }
  // ---- 光照查询（支持 A2 和 lux 两种标签）----
  if (0 == strcmp("A2", ptag) || 0 == strcmp("lux", ptag)) {
    if (0 == strcmp("?", pval)) {
      updateA2();
      ret = sprintf(p, "%.1f", A2);
      ZXBeeAdd("lux", p);
    }
  }

  // ---- 上报周期 V0 ----
  if (0 == strcmp("V0", ptag)) {
    if (0 == strcmp("?", pval)) {
      ret = sprintf(p, "%u", V0);
      ZXBeeAdd("V0", p);
    } else {
      updateV0(pval);
    }
  }
  return ret;
}

/*********************************************************************************************
* 名称：MyEventProcess()
* 功能：OSAL 事件处理函数，驱动定时上报和周期检查
* 参数：event -- 事件编号
* 返回：无
*********************************************************************************************/
void MyEventProcess(uint16 event)
{
  static char check_start_flag = 0;

  // 首次 MY_REPORT_EVT 时启动 MY_CHECK_EVT 定时器
  if (check_start_flag == 0) {
    if (event & MY_REPORT_EVT) {
      osal_start_timerEx(sapi_TaskID, MY_CHECK_EVT, 100);
      check_start_flag = 1;
    }
  }

  // 定时上报事件：采集 temp/humi/lux 并发送
  if (event & MY_REPORT_EVT) {
    sensorUpdate();
    osal_start_timerEx(sapi_TaskID, MY_REPORT_EVT, V0 * 1000); // V0 秒后再次触发
  }

  // 周期检查事件（100ms）
  if (event & MY_CHECK_EVT) {
    sensorCheck();
    osal_start_timerEx(sapi_TaskID, MY_CHECK_EVT, 100);
  }
}
