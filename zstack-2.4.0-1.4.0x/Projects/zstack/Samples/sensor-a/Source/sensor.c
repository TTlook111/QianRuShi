static uint8 D0 = 0xFF;                                         // D0 传感器数据主动上报使能位
static float A0 = 0.0;                                          // A0 温度值（对应项目字段 temp）
static float A1 = 0.0;                                          // A1 湿度值（对应项目字段 humi）
static float A2 = 0.0;                                          // A2 光照值（对应项目字段 lux）
static uint16 V0 = 30;                                          // V0 数据上报周期，默认30秒

/*********************************************************************************************
* 名称：updateV0()
* 功能：更新V0上报周期的值
* 参数：*val -- 指向存放数据的指针
* 返回：无
*********************************************************************************************/
void updateV0(char *val)
{
  V0 = atoi(val);                                               // 将字符串转换为整型上报周期（秒）
}

/*********************************************************************************************
* 名称：updateA0()
* 功能：读取 HTU21D 温度传感器，更新 A0（temp）
* 参数：无
* 返回：无
*********************************************************************************************/
void updateA0(void)
{
  A0 = (htu21d_get_data(TEMP) / 100.0f);                        // 读取温度，单位摄氏度
}

/*********************************************************************************************
* 名称：updateA1()
* 功能：读取 HTU21D 湿度传感器，更新 A1（humi）
* 参数：无
* 返回：无
*********************************************************************************************/
void updateA1(void)
{
  A1 = (htu21d_get_data(HUMI) / 100.0f);                        // 读取湿度，单位 %RH
}

/*********************************************************************************************
* 名称：updateA2()
* 功能：读取 BH1750 光照传感器，更新 A2（lux）
* 参数：无
* 返回：无
*********************************************************************************************/
void updateA2(void)
{
  A2 = bh1750_get_data();                                       // 读取光照强度，单位 lux
}

/*********************************************************************************************
* 名称：sensorInit()
* 功能：初始化环境采集传感器及定时器
* 参数：无
* 返回：无
*********************************************************************************************/
void sensorInit(void)
{
  ZXBeeInfSetLocalAddr(ZXBEE_ADDR_SENSOR_A);                    // 设置本节点地址为 sensor-a (0x01)

  // 初始化 IIC 总线传感器
  htu21d_init();                                                // HTU21D 温湿度传感器初始化
  bh1750_init();                                                // BH1750 光照传感器初始化

  // 启动定时上报定时器，随机延迟避免多个节点同时上报
  osal_start_timerEx(sapi_TaskID, MY_REPORT_EVT, (uint16)((osal_rand() % 10) * 1000));
}

/*********************************************************************************************
* 名称：sensorLinkOn()
* 功能：节点加入网络后立即上报一次环境数据
* 参数：无
* 返回：无
*********************************************************************************************/
void sensorLinkOn(void)
{
  sensorUpdate();                                               // 入网后立即上报当前环境数据
}

/*********************************************************************************************
* 名称：sensorUpdate()
* 功能：定时采集环境传感器数据并通过 ZXBee 上报
*       上报字段：temp（温度）、humi（湿度）、lux（光照）
*       上报格式：{"temp":27.0,"humi":65.0,"lux":200}
* 参数：无
* 返回：无
*********************************************************************************************/
void sensorUpdate(void)
{
  char pData[64];                                               // 缓冲区用于格式化传感器数值
  char *p = pData;

  ZXBeeBegin();                                                 // 构建 ZXBee JSON 帧头

  // D0 bit0 = 1：上报温度
  if ((D0 & 0x01) == 0x01) {
    updateA0();
    sprintf(p, "%.1f", A0);
    ZXBeeAdd("temp", p);                                        // 项目字段：temp
  }
  // D0 bit1 = 1：上报湿度
  if ((D0 & 0x02) == 0x02) {
    updateA1();
    sprintf(p, "%.1f", A1);
    ZXBeeAdd("humi", p);                                        // 项目字段：humi
  }
  // D0 bit2 = 1：上报光照
  if ((D0 & 0x04) == 0x04) {
    updateA2();
    sprintf(p, "%.1f", A2);
    ZXBeeAdd("lux", p);                                         // 项目字段：lux
  }

  p = ZXBeeEnd();                                               // 结束 JSON 帧
  if (p != NULL) {
    ZXBeeInfSend(p, strlen(p));                                 // 通过 ZigBee 发送环境数据
  }
}

